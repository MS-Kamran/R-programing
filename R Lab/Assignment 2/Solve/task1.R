Marks=readline(print("Enter Your Marks: "))
if(Marks>=90){
  print("A")
}else if(90>Marks && Marks>=85){
  print("A-")
}else if(95>Marks && Marks>=80){
  print("B+")
}else if(80>Marks && Marks>=75){
  print("B")
}else if(75>Marks && Marks>=70){
  print("B-")
}else if(70>Marks && Marks>=65){
  print("C+")
}else if(65>Marks && Marks>=60){
  print("C")
}else if(60>Marks && Marks>=55){
  print("C-")
}else if(Marks<55){
  print("Fail")
}
